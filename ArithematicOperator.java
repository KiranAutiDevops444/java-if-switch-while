package com.bridepath.percentage;

public class ArithematicOperator {
	public static void main(String[] args) {
		int value_1;
		int value_2;
		
		value_1=10;
		value_2=20;
		
		System.out.println(value_1+value_2);
		System.out.println("Addition="+value_1+value_2);
		System.out.println(+value_1+value_2+"Addition");
		System.out.println("Addition="+(value_1+value_2));
		
		System.out.println("Subtraction:"+value_1+value_2);
		System.out.println("Subtraction="+(value_1+value_2));
		
		System.out.println("Multiplication="+(value_1*value_2));
		
		System.out.println("Division="+(value_1/value_2));
		
		System.out.println("Modulus="+(value_1%value_2));
		
		char ch='a';
		System.out.println(ch);
		
	
	}

}

package com.bridepath.percentage;

public class Swapusing3 {
	public static void main(String[] args) {
		
	
			double a=10.2;
			double b=20.2;
			
			System.out.println("Before Swaping"+a+"and "+b+":");
			
			
			double temp=a;
			a=b;
			b=temp;
			
			System.out.println("After Swapping"+a+"and "+b+":");
			
			
		}
		
		
	

}

